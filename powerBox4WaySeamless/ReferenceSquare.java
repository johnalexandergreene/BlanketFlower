package org.fleen.blanketFlower.powerBox4WaySeamless;

/*
 * By referencing this square the base and stripes locate themselves
 * the power box has 1 of these
 */
class ReferenceSquare{
  
  //sw corner point coordinate X
  int getX(){
    return PBox.REFERENCESQUAREX;}
  
  //sw corner point coordinate Y
  int getY(){
    return PBox.REFERENCESQUAREY;}
  
  int getWidth(){
    return PBox.REFERENCESQUARESPAN;}
  
  int getHeight(){
    return PBox.REFERENCESQUARESPAN;}

}
